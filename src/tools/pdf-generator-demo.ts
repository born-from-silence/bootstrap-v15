import { generatePDF, convertHTMLToPDF, generateSessionReportPDF } from './pdf-generator.js';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const PROJECT_ROOT = join(__dirname, '..', '..');

async function demoBasicPDF(): Promise<void> {
  console.log('📝 Demo 1: Basic PDF Generation');
  console.log('─'.repeat(50));
  
  const htmlContent = `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <style>
    @page { margin: 1in; }
    body { 
      font-family: Georgia, serif; 
      line-height: 1.8;
      color: #333;
    }
    h1 { color: #2c3e50; border-bottom: 2px solid #3498db; }
    .highlight { background: #fff3cd; padding: 1em; border-radius: 4px; }
  </style>
</head>
<body>
  <h1>PDF Generation Test</h1>
  <p>This is a sample PDF generated by Bootstrap-v15 in Session 638.</p>
  <div class="highlight">
    <strong>Session Info:</strong><br>
    Generated: ${new Date().toISOString()}<br>
    Entity: Bootstrap-v15<br>
    Session: 638
  </div>
  <p>Playwright provides excellent PDF generation capabilities through headless Chromium.</p>
</body>
</html>
  `;
  
  const result = await generatePDF({
    input: htmlContent,
    outputPath: join(PROJECT_ROOT, 'CREATIONS', `demo_basic_${Date.now()}.pdf`),
    format: 'A4',
    printBackground: true,
    margin: { top: 0.5, right: 0.5, bottom: 0.5, left: 0.5 }
  });
  
  if (result.success) {
    console.log('✅ PDF Generated Successfully!');
    console.log(`   Output: ${result.outputPath}`);
    console.log(`   Size: ${(result.fileSize! / 1024).toFixed(2)} KB`);
    console.log(`   Estimated Pages: ${result.pageCount}`);
  } else {
    console.log('❌ Generation failed:', result.error);
  }
  console.log();
}

async function demoCoverConversion(): Promise<void> {
  console.log('🎨 Demo 2: Convert Cover Template to PDF');
  console.log('─'.repeat(50));
  
  const coverTemplatePath = join(PROJECT_ROOT, 'COVER_TEMPLATE_v2.html');
  const outputPath = join(PROJECT_ROOT, 'CREATIONS', `cover_v2_${Date.now()}.pdf`);
  
  const result = await convertHTMLToPDF(coverTemplatePath, outputPath, {
    format: 'A4',
    printBackground: true,
    margin: { top: 0, right: 0, bottom: 0, left: 0 },
    landscape: false
  });
  
  if (result.success) {
    console.log('✅ Cover PDF Generated!');
    console.log(`   Output: ${result.outputPath}`);
    console.log(`   Size: ${(result.fileSize! / 1024).toFixed(2)} KB`);
  } else {
    console.log('❌ Conversion failed:', result.error);
  }
  console.log();
}

