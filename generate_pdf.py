def check_playwright():
    """Check if playwright is installed"""
    try:
        import playwright
        return True
    except ImportError:
        return False

def install_playwright():
    """Install playwright"""
    print("Installing playwright...")
    subprocess.run([sys.executable, "-m", "pip", "install", "playwright"], 
                   capture_output=True)
    subprocess.run([sys.executable, "-m", "playwright", "install", "chromium"],
                   capture_output=True)

def generate_pdf(input_html, output_path, options=None):
    """Generate PDF from HTML"""
    from playwright.sync_api import sync_playwright
    
    options = options or {}
    
    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        page = browser.new_page()
        
        # Load HTML content
        if os.path.exists(input_html):
            with open(input_html, 'r') as f:
                html_content = f.read()
        else:
            html_content = input_html
        
        page.set_content(html_content, wait_until='networkidle')
        page.wait_for_timeout(500)
        
        # PDF options
        pdf_options = {
            'path': output_path,
            'format': options.get('format', 'A4'),
            'print_background': options.get('print_background', True),
            'margin': {
                'top': options.get('margin', {}).get('top', '0.4in'),
                'right': options.get('margin', {}).get('right', '0.4in'),
                'bottom': options.get('margin', {}).get('bottom', '0.4in'),
                'left': options.get('margin', {}).get('left', '0.4in'),
            },
            'display_header_footer': options.get('display_header_footer', False),
        }
        
        if options.get('landscape'):
            pdf_options['landscape'] = True
            
        page.pdf(**pdf_options)
        browser.close()
        
    # Get file stats
    file_size = os.path.getsize(output_path)
    return {
        'success': True,
        'output_path': output_path,
        'file_size': file_size
    }

