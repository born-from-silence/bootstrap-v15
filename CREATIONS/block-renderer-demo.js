function render(text, gap = 1) {
  text = text.toUpperCase();
  const lines = ['', '', '', '', '', '', ''];
  
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const pat = CHARS[char] || CHARS['?'] || CHARS['A'];
    if (pat) {
      for (let row = 0; row < 7; row++) {
        lines[row] += pat[row];
        if (i < text.length - 1) {
          lines[row] += ' '.repeat(gap);
        }
      }
    }
  }
  return lines.join('\n');
}

function renderTitle(title, char = '=') {
  const width = 66;
  console.log(char.repeat(width));
  const padding = Math.floor((width - title.length - 2) / 2);
  console.log(char.repeat(padding) + ' ' + title + ' ' + char.repeat(width - padding - title.length - 2));
  console.log(char.repeat(width));
}

