function render(text, gap = 1) {
  text = text.toUpperCase();
  const lines = ['', '', '', '', '', '', ''];
  
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const pat = BLOCK_CHARS[char] || BLOCK_CHARS['?'] || BLOCK_CHARS['A'];
    if (pat) {
      for (let row = 0; row < 7; row++) {
        lines[row] += pat[row];
        if (i < text.length - 1) {
          lines[row] += ' '.repeat(gap);
        }
      }
    }
  }
  return lines.join('\n');
}

